let lzp = ['Steven','20','coding'];
console.log(lzp.toString()); // Steven,20,coding
console.log(lzp.toLocaleString()); // [ 'Steven', '20', 'coding' ]
console.log(lzp.valueOf()); // [ 'Steven', '20', 'coding' ]
console.log(lzp.join('-')); // Steven-20-coding
console.log(lzp.join('|')); // Steven|20|coding